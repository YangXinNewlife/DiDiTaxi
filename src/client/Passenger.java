package client;

public class Passenger {
	
	private int dst_X;
	private int dst_y;
	private int src_X;
	private int src_y;
	
	public int getDst_X() {
		return dst_X;
	}
	public void setDst_X(int dst_X) {
		this.dst_X = dst_X;
	}
	public int getDst_y() {
		return dst_y;
	}
	public void setDst_y(int dst_y) {
		this.dst_y = dst_y;
	}
	public int getSrc_X() {
		return src_X;
	}
	public void setSrc_X(int src_X) {
		this.src_X = src_X;
	}
	public int getSrc_y() {
		return src_y;
	}
	public void setSrc_y(int src_y) {
		this.src_y = src_y;
	}
}
